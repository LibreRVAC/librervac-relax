#include <8051.h>
#include "uart.h"
#include "timing.h"

void setup() {
    TMOD = 0b0001       // timer 0 mode 1 16-bit timer
        | (0b0010 << 4); // timer 1 mode 2  8-bit auto reload
    setup_uart();
    setup_timing();
    EA = 1; // Open master interrupt switch
}
